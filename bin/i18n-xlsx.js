#!/usr/bin/env node

const path = require('path')
const fs = require('fs')
const Program = require('commander')
const chalk = require('chalk')
const Log = console.log
const { exec, execSync } = require('child_process')

Program
  .version('1.0.0')
  .usage('[options] [value ...]')
  .option('-p, --path [value]', '', '')
  .option('-b, --branch [value]', '', '')
  .option('-i, --ignore-branch [value]', '', '')
  .option('-c, --current-branch', '')
  .parse(process.argv)

let pwd = process.cwd()
if (!Program.path) {
  ;(function () {
    while (pwd.split(path.sep).filter(p => p).length !== 1) {
      if (fs.readdirSync(pwd).includes('package.json')) {
        return
      }
      pwd = path.resolve(pwd, '..')
    }
    Log(chalk.red('Error: Not a node repository (or any of the parent directories): package.json'))
    process.exit(1)
  })()
}

let rootPath = Program.path || pwd
let includeBranch = Program.branch.split(',').filter(b => b)
let ignoreBranch = Program.ignoreBranch.split(',')
let isCurrentBranch = Program.currentBranch

// Get i18ns only from current branch
if (isCurrentBranch) {
  require(path.join(__dirname, '../index.js'))(rootPath)
// Get i18ns from different branchs
} else {
  process.chdir(rootPath)
  let branchsStr = execSync('git branch -l').toString()
  let branchs = branchsStr.split(/\n/).map(s => s.replace('*', '').trim()).filter(s => s)
  let currentBranch = branchsStr.match(/\*\s\b(.*)\b/m)[1]
  let statusStr = execSync('git status').toString()
  let shouldStash = !statusStr.includes('nothing to commit')
  if (shouldStash) {
    execSync('git add . & git stash')
  }
  Log(chalk.blue('Current branch', currentBranch))

  let failCount = 0
  let startTime = Date.now()
  for (let br of branchs) {
    if (ignoreBranch.includes(br) ||
      (includeBranch.length && !includeBranch.includes(br))
    ) {
      continue
    }
    Log(chalk.blue('Next branch', br))

    // Checkout different branchs
    let coSucc = false
    while (!coSucc) {
      try {
        execSync(`git checkout ${br}`)
        coSucc = true
      } catch (e) {
        // Sometimes occurs 'Permission denied' in windows
        execSync('git reset --hard & git clean -df')
      }
    }

    // Collect all i18ns from different branchs into one excel
    let mainSucc = false
    while (!mainSucc) {
      try {
        require(path.join(__dirname, '../index.js'))(rootPath)
        mainSucc = true
      } catch (e) {
        Log(chalk.red('Fail times: EPERM: ', ++failCount))
      }
    }

    // Clean all .js and .xlsx generated by vue-i18n-xlsx
    let rsSucc = false
    while (!rsSucc) {
      try {
        execSync('git reset --hard & git clean -df')
        rsSucc = true
      } catch (e) {
        // noop
      }
    }
  }

  // Checkout original branch
  let currSucc = false
  while (!currSucc) {
    try {
      execSync(`git checkout ${currentBranch}`)
      if (shouldStash) {
        execSync(`git stash apply`)
      }
      currSucc = true
    } catch (e) {
      execSync(`git reset --hard & git clean -df`)
    }
  }
  process.chdir(__dirname)
  Log(chalk.green(
    chalk.bgGreen.black(' ALL DONE '),
    `Generate js & excel from different branchs successfully in ${(Date.now() - startTime) / 1000}s`
  ))
}

